package John.DataTypesVariablesAndClasses;

public class PrimitiveDataTypes_SticksAndStones {

  // -128 to 127
  byte myByte = 12;

  // -32,768 to 32,767
  short myShort = 1234;

  // -2,147,483,648 to 2,147,483,647
  int myInt = 2837;

  // -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
  long myLong = 12323;

  // about 6 decimal digits
  // need to put f after, because by default, degit number is doulbe for JAVA
  float myFloat = 6.3f;

  // about 15 decimal digits 
  double myDouble = 45.213434;

  // true of false
  boolean myBoolean = true;
  boolean moreThan1000 = (myInt > 1000); // true

  // one character
  char myChar = 'W';

  // P.S String is not a primitive!

  
}
