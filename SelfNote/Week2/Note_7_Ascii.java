public class Note_7_Ascii {

  public static void main(String[] args) {

    // Each character have its own value in the Ascii table. 
    // 'a' = 97, 'b' = 98...etc

    // Print ASCII value of a character - First Approach
    
    char c = 'a';

    int n;

    n = c;

    System.out.println(n); // 97

    // Second Approach

    char d = 'a';

    System.out.println((int) d); // 97
  }
  
}
