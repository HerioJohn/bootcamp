public class c3_33_ExchangeElements {

  public static void main(String[] args) {

  int a = 5;
  int b = 6;

  // How to exchange them?

  int temp = b; // a = 5, b = 6, temp = 5
  a = b; //a = 6, b = 6, temp = 5;
  b = temp; // a = 6, b = 5, temp = 5;
    
  }
  
}
