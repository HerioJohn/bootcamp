package Udemy.Chapter_1.Chapter_3;

public class c3_38_ModOperator {

  public static void main(String[] args) {
    
    // mod operator, remainder operator

    System.out.println(10 % 3); // 10 / 3 的餘數: 1

    int a = 154; 

    if (a % 2 == 0) 
      System.out.println("a is an even number!");
    else
      System.out.println("a is not an even number!");


  }
  
}
