public class c3_35_Math {

  public static void main(String[] args) {

    int x = 20;
    int y = 4;
    double z = 3.14;

    int a = 9;

    System.out.println(Math.max(x, y)); // return the bigger number

    System.out.println(Math.ceil(z)); // // 4.0

    System.out.println(Math.sqrt(a)); // 3.0

  }
  
}
